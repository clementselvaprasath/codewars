function findUniq(arr) {
    unique = null
    seen = new Set()

    for (var i of arr) {
        if (!seen.has(i)) {
            unique = i
            seen.add(i)
        }
    }
    return unique
}

// ----------------------- //
// Tests
// ----------------------- //
function test(input, expected) {
    result = findUniq(input)
    
    console.log("Testing with " + input + ".")
    if (result === expected) {
        console.log("Test passed!")
    } else {
        console.log("Test failed. :(")
        console.log(" => Got:      " + result)
        console.log("    Expected: " + expected)
    }
    console.log()
}

